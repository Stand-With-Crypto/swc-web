Cypress.Commands.add('selectFromComboBox', ({ trigger, searchText }) => {
  trigger.click()
  cy.get('[cmdk-input]').clear().type(searchText, { timeout: 1000 })
  cy.get('[cmdk-group-items]').first().click()
})

export {}

